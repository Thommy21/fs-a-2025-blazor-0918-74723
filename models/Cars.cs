
public class Cars
{
    public int Id { get; set; }            // Unique identifier
    public string Make { get; set; }       // e.g. Toyota, BMW
    public string Model { get; set; }      // e.g. Corolla, 3 Series
    public int Year { get; set; }          // Manufacturing year
    public string Color { get; set; }      // Vehicle color
    public string LicensePlate { get; set; } // Registration plate
    public int Mileage { get; set; }       // km or miles
}

